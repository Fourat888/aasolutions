let currentLang = localStorage.getItem('lang') || 'EN';

function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const attr = el.getAttribute('data-i18n-attr');

        const translation = translations[lang]?.[key] || translations['EN']?.[key] || key;

        if (attr) {
            el.setAttribute(attr, translation);
        } else {
            el.innerHTML = translation;
        }
    });

    // Update flag and label in dropdown too
    const currentFlag = document.getElementById('currentFlag');
    const currentLabel = document.getElementById('currentLangLabel');
    const altFlag = document.getElementById('altFlag');
    const altLabel = document.getElementById('altLangLabel');

    if (lang === 'FR') {
        currentFlag.src = 'images/fr_FR.png';
        currentLabel.textContent = 'FRANÇAIS';
        altFlag.src = 'images/en_US.png';
        altLabel.textContent = 'ENGLISH';
    } else {
        currentFlag.src = 'images/en_US.png';
        currentLabel.textContent = 'ENGLISH';
        altFlag.src = 'images/fr_FR.png';
        altLabel.textContent = 'FRANÇAIS';
    }

    localStorage.setItem('lang', lang);
}

function switchLanguage() {
    currentLang = (currentLang === 'EN') ? 'FR' : 'EN';
    applyTranslations(currentLang);
}

// Initial load
document.addEventListener("DOMContentLoaded", () => {
    applyTranslations(currentLang);
});

